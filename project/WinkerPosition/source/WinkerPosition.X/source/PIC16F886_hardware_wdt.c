/*------------------------------------------------------------------------------
* OverView	: WDT初期化
* Parameter	: None
* Return	: None
*-----------------------------------------------------------------------------*/
void HW_WDT_Initialize( void )
{
	/* 1:1,1:128 → 4.129032msec */
	REG_WRITE_08( WDTCON, 0x05 );
}

/*------------------------------------------------------------------------------
* OverView	: 点滅設定
* Parameter	: None
* Return	: None
*-----------------------------------------------------------------------------*/
void HW_WDT_Clear( void )
{
	CLRWDT();
}


